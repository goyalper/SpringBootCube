package com.cubemasterpro.model;

/**
 * Enum representing the type of move (clockwise, counter-clockwise, or double).
 */
public enum MoveType {
    CLOCKWISE,          // Represented as the face letter alone (e.g., "R")
    COUNTER_CLOCKWISE,  // Represented with a prime symbol (e.g., "R'")
    DOUBLE              // Represented with a 2 (e.g., "R2")
}
